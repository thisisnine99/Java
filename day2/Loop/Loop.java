package day2.Loop;

public class Loop {
    public static void main(String[] args) {

        // 반복문

        // while, for

        // while
        System.out.println("---while문---");

        int i = 0;
        while (i < 10) {
            System.out.println("안녕");
            i++;
        }

        // for
        System.out.println("---for문---");
        for (int j = 0; j < 10; j++) {
            System.out.println("안녕");
        }
    }
}
